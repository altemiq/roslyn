            }
            """,
            new DiagnosticResult("CS1591", Microsoft.CodeAnalysis.DiagnosticSeverity.Warning)
                .WithLocation(5, 18)
                .WithIsSuppressed(true));
        yield return () => (
            """
            // <auto-generated />

            namespace MyNamespace
            {
                public class UnDocumentedClass
                {
                }
            }
            """,
            new DiagnosticResult("CS1591", Microsoft.CodeAnalysis.DiagnosticSeverity.Warning)
                .WithLocation(5, 18)
                .WithIsSuppressed(true));
        yield return () => (
            """
            // <auto-generated/>

            namespace MyNamespace
            {
                public class UnDocumentedClass
                {
                }
            }
            """,
            new DiagnosticResult("CS1591", Microsoft.CodeAnalysis.DiagnosticSeverity.Warning)
                .WithLocation(5, 18)
                .WithIsSuppressed(true));
        yield return () => (
            """
